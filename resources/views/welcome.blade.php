            <h2 id="backend-coding-challenge-instructions">Backend Coding Challenge Instructions</h2>
            <h4 id="description">Description</h4>
            <p>Design a public API for a project tracker app. Programmers can have multiple projects that work on and projects can belong to multiple programmers. Programmers can be assigned to or removed from projects.</p>
            <h4 id="tasks">Tasks</h4>
            <ul>
            <li>Create programmers model. Programmers have ‘name’ &amp; ’email’ fields.</li>
            <li>Create project model. Projects have ‘title’, ‘startDate’, ‘deadLineDate’ &amp; ‘budget’</li>
            <li>Create programmers endpoint for getting a list of all the programmers. </li>
            <li>Create programmer CRUD endpoints.</li>
            <li>Create projects endpoint for getting a list of all the projects. </li>
            <li>Create project CRUD endpoint.</li>
            <li>Create endpoints for assigning-removing a programmer to/from a project.</li>
            <li>In programmers endpoint results you have to include the projects that are assigned to programmers and the date assigned to them.</li>
            <li>All resources must implement CRUD endpoints</li>
            </ul>
            <h4 id="deliverables">Deliverables</h4>
